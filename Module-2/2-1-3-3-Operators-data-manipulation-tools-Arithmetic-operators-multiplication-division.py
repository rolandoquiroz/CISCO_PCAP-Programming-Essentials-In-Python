#!/usr/bin/python3
"""
An * (asterisk) sign is a multiplication operator.
For multiplication It's possible to formulate the following rules
based on this result:
-When both ** arguments are integers, the result is an integer, too;
-When at least one ** argument is a float, the result is a float, too

A / (slash) sign is a divisional operator.
The value in front of the slash is a dividend,
the value behind the slash, a divisor.
The result produced by the division operator is always, always a float,
"""
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3.)

print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.)

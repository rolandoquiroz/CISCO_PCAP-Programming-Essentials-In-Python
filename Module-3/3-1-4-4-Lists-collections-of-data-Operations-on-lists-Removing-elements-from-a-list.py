#!/usr/bin/python3
"""
It may look strange, but negative indices are legal,
and can be very useful.

An element with an index equal to -1 is the last one in the list.

Similarly, the element with an index equal to -2 is the one before
last in the list.
"""
numbers = [111, 7, 2, 1]
print(numbers[-1])
print(numbers[-2])
